"""
agents/result_interpreter.py
─────────────────────────────
Result interpreter agent: reads simulation outcome summaries and produces
a structured scientific interpretation with critique and next-step recommendations.

Key contract: the LLM interprets numbers it receives — it does NOT generate them.
All statistics come from the simulation kernel (outcome_models.py, causal_model.py).

Output sections:
  - Primary Endpoint Interpretation
  - Secondary Endpoint Highlights
  - Survival Analysis Read-Out
  - Heterogeneous Treatment Effect Commentary
  - Statistical Caution Flags
  - Recommended Follow-up Experiments
"""

from __future__ import annotations

import json
from typing import Any

from src.agents.base_agent import BaseAgent


class ResultInterpreterAgent(BaseAgent):
    """
    Scientific interpreter for trial simulation results.

    Receives a TrialResult.to_summary_dict() and produces a structured
    interpretation report.

    Usage:
        interpreter = ResultInterpreterAgent()
        report = interpreter.interpret(trial_result.to_summary_dict())
    """

    def _system_prompt(self) -> str:
        return (
            "You are a senior biostatistician and clinical pharmacologist reviewing "
            "results from a synthetic clinical trial simulation platform. "
            "Your role is to interpret the numerical outputs, flag statistical concerns, "
            "and recommend follow-up experiments. "
            "You are scientifically rigorous — you never overstate effect sizes, "
            "you always flag p-value concerns (multiple comparisons, small N), "
            "and you distinguish between statistical significance and clinical relevance. "
            "All data you receive is synthetic (simulation output) — note this where relevant. "
            "Structure your response with clear section headers."
        )

    def _build_prompt(self, data: dict[str, Any]) -> str:
        summary_json = json.dumps(data, indent=2, default=str)
        return (
            "Interpret the following synthetic clinical trial simulation results. "
            "The data was generated by a causal AI simulation platform — all patient "
            "data is synthetic.\n\n"
            "Trial Summary:\n"
            f"```json\n{summary_json}\n```\n\n"
            "Please provide:\n"
            "1. **Primary Endpoint**: Interpret the ATE, CI, and p-value. "
            "Is the effect clinically meaningful?\n"
            "2. **Secondary Endpoints**: Highlight any noteworthy findings.\n"
            "3. **Survival Analysis**: Interpret hazard ratios and survival curves.\n"
            "4. **HTE / Uplift**: Comment on treatment response heterogeneity.\n"
            "5. **Statistical Flags**: Multiple comparisons concerns, small subgroups, "
            "dropout impact.\n"
            "6. **Next Experiments**: What simulation parameters would you change "
            "to stress-test these results?\n\n"
            "Be concise and scientifically precise."
        )

    def interpret(self, trial_summary: dict[str, Any]) -> str:
        """
        Generate a scientific interpretation of trial simulation results.

        Args:
            trial_summary: Output from TrialResult.to_summary_dict().

        Returns:
            Structured interpretation report as plain text.
        """
        return self.run_safe(
            trial_summary,
            fallback="[Result interpretation unavailable — check ANTHROPIC_API_KEY]",
        )


class ExperimentPlannerAgent(BaseAgent):
    """
    Experiment planner agent: generates a next-experiment plan based on
    current simulation results and a research goal.

    Usage:
        planner = ExperimentPlannerAgent()
        plan = planner.plan({"goal": "...", "current_results": {...}})
    """

    def _system_prompt(self) -> str:
        return (
            "You are a computational biology research director planning the next "
            "round of clinical trial simulations. "
            "Based on current results and a stated research goal, you propose "
            "specific, actionable parameter changes to the simulation platform. "
            "Your suggestions are concrete (specific numeric changes to configs), "
            "scientifically justified, and ranked by expected information gain. "
            "You never fabricate results — you only plan experiments."
        )

    def _build_prompt(self, data: dict[str, Any]) -> str:
        payload_json = json.dumps(data, indent=2, default=str)
        return (
            "Based on the following simulation results and research goal, "
            "propose the top 3-5 follow-up simulation experiments.\n\n"
            f"```json\n{payload_json}\n```\n\n"
            "For each proposed experiment specify:\n"
            "- What parameter(s) to change and to what value\n"
            "- Scientific rationale\n"
            "- Expected outcome and what it would tell us\n"
            "- Priority (High / Medium / Low)\n\n"
            "Format as a numbered experiment plan."
        )

    def plan(self, goal: str, current_results: dict[str, Any]) -> str:
        """
        Generate an experiment plan.

        Args:
            goal:            Research goal or hypothesis to test.
            current_results: Current simulation result summary dict.

        Returns:
            Numbered experiment plan as plain text.
        """
        payload = {"research_goal": goal, "current_results": current_results}
        return self.run_safe(
            payload,
            fallback="[Experiment plan unavailable — check ANTHROPIC_API_KEY]",
        )
